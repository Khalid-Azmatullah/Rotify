function skipTrack(direction, currentTrack) {
    console.log(typeof currentTrackIndex)
    if (typeof currentTrack === "undefined") {
        console.log('y')
        currentTrack = (currentTrackIndex + direction + tracks.length) % tracks.length
        console.log(currentTrack)
        currentTrackIndex = currentTrack
    }
    audio.src = 'Songs/' + tracks[currentTrack].src;
    document.querySelector('.player-track-title-mini').textContent = tracks[currentTrack].title;
    document.querySelector('.player-track-title-full').textContent = tracks[currentTrack].title;
    document.querySelector('.player-artist-name-mini').textContent = tracks[currentTrack].artist;
    document.querySelector('.player-artist-name-full').textContent = tracks[currentTrack].artist; 
    playPauseBtn.click()
    audio.play();
    updateMediaSession();
}

function formatTime(seconds) {
    const minutes = Math.floor(seconds / 60);
    const secs = Math.floor(seconds % 60);
    return `${minutes}:${secs < 10 ? '0' : ''}${secs}`;
}

function updateMediaSession() {
    if ('mediaSession' in navigator) {
        navigator.mediaSession.metadata = new MediaMetadata({
        title: tracks[currentTrackIndex].title,
        artist: tracks[currentTrackIndex].artist,
        // artwork: [
        //    { src: tracks[currentTrackIndex].artwork[0], sizes: '96x96', type: 'image/jpeg' },
        //    { src: tracks[currentTrackIndex].artwork[1], type: 'image/jpeg' },
        //    { src: tracks[currentTrackIndex].artwork[2], sizes: '192x192', type: 'image/jpeg' }
        // ]
        });

        navigator.mediaSession.setActionHandler('play', () => {
            playPauseBtn.click()
            audio.play()
        });
        navigator.mediaSession.setActionHandler('pause', () => {
            playPauseBtn.click()
            audio.pause()
        });
        navigator.mediaSession.setActionHandler('previoustrack', () => skipTrack(-1));
        navigator.mediaSession.setActionHandler('nexttrack', () => skipTrack(1));
    }
}


